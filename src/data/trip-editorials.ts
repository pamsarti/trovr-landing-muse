/** Extract price symbol (€€/€€€/€€€€) from messy price_range field, fallback to a generic band. */
export function priceSymbol(priceRange: string): string {
  const m = priceRange.match(/€+(?:-€+)?/);
  if (m) return m[0];
  // crude inference from USD/CNY strings
  const usd = priceRange.match(/\$?\s?(\d[\d,]*)/);
  if (usd) {
    const n = parseInt(usd[1].replace(/,/g, ""), 10);
    if (n >= 4000) return "€€€€";
    if (n >= 1500) return "€€€";
    if (n >= 500) return "€€";
    return "€";
  }
  return "€€–€€€";
}